export const getRiddlesByDifficulty = (difficulty: Riddle['difficulty']): Riddle[] => {
  return riddles.filter(riddle => riddle.difficulty === difficulty);
};

export const getRandomRiddlesByDifficulty = (difficulty: Riddle['difficulty'], count: number = 10): Riddle[] => {
  const difficultyRiddles = getRiddlesByDifficulty(difficulty);
  const shuffled = [...difficultyRiddles].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
};
